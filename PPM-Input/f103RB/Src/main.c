uint32_t Check_Int_WrapAround(uint32_t main_elapsed)
{
	if (main_elapsed < 0)
	{
		return (TIM1->ARR - channelTimer) + main_elapsed;
	}
	else
	{
		return main_elapsed;
	}
}

