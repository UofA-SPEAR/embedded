// Override exti handler
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	// figure out which event to start by pin state then call function
	if (GPIOC -> IDR & GPIO_Pin) // rising edge since bit is high
	{
		Interupt_Rising(GPIO_Pin);
	}
	else
	{
		Interupt_Falling(GPIO_Pin);
	}
}

void Interupt_Rising(uint16_t GPIO_Pin)
{
	// code for handling a rising edge
	// record time
	channelTimer = TIM1->CNT;
	if (!startTimer)
	{
		// if time elapsed is 0 set time to start
		startTimer = channelTimer;
	}
}

void Interupt_Falling(uint16_t GPIO_Pin)
{
	// code for handling a falling edge
	// get value of ppm
	uint32_t elapsed = Check_Int_WrapAround(TIM1->CNT - channelTimer) - TICKS_PER_MS; // need to put in range of 0-100 from 1-2ms
	if (elapsed < 0)
	{
		elapsed = 0;
	}
	else if (elapsed > 100)
	{
		elapsed = 100;
	}
	// save the value depending on channel count
	ppmData.data[channel] = elapsed;
	// increment channel count
	channel++;
}

void Reset_State()
{
	// code for sending, and resetting data
	Transmit_Data();
	// reset stuff
	startTimer = 0;
	channelTimer = 0;
	channel = 0;
	ppmData.channels = 0;
	for (int i = 0; i < MAX_NUM_OF_CHANNELS; i++)
	{
		ppmData.data[i] = 200;
	}
}

